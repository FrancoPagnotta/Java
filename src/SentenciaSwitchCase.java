public class SentenciaSwitchCase {
    public static void main(String[] args) {

        int num = 3;

        switch (num) {
            case 0:
                System.out.println("El numero es " + num);
                break;
            case 1:
                System.out.println("El numero es " + num);
                break;
            case 2:
                System.out.println("El numero es " + num);
                break;
            case 3:
                System.out.println("El numero es " + num);
                break;
        }
    }
}
