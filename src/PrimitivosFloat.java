public class PrimitivosFloat {

    public static void main(String[] args) {
        
        float realFloat = 2120.0F;
        System.out.println("realFloat = " + realFloat);

        System.out.println("float type in bytes correspond to " + Float.BYTES);
        System.out.println("float in bits is equal to " + Float.SIZE);
        System.out.println("Max value of float type is " + Float.MAX_VALUE);
        System.out.println("Min value of float type is " + Float.MIN_VALUE);


        double realDouble = 3.4028235E39;
        System.out.println("realDouble = " + realDouble);

        System.out.println("double type in bytes correspond to " + Double.BYTES);
        System.out.println("double in bits is equal to " + Double.SIZE);
        System.out.println("Max value of double type is " + Double.MAX_VALUE);
        System.out.println("Min value of double type is " + Double.MIN_VALUE);

        float varFloat = 3.1416F;
        System.out.println("varFloat = " + varFloat);

    }
}


